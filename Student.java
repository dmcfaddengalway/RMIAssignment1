public class Student {

  // Member Variables
  private String password;
  private int id;

  // Constructor
  public Student(String pw, int idNo) {
    this.password = pw;
    this.id = idNo;
  }

  // Methods

  // Accessor Methods

    // Setters

    // Getters
    public String getPassword() {
      return password;
    }

    public int getId() {
      return id;
    }

}
